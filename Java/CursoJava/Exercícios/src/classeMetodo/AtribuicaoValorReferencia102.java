package classeMetodo;

public class AtribuicaoValorReferencia102 {
	/**
	 * A atribuição por valor é feita para tipos primitivos
	 * ao criar uma variável, na memória do computador será guardado
	 * o valor da variável.
	 * 
	 * Atribuição por referência
	 * Feita para objetos
	 * Ao se criar um objeto, na memória do computador não haverá o valor da variável
	 * e sim um endereço para uma área de memória que contém o valor.
	 * Ao se fazer uma cópia de um objeto, a cópia recebe o mesmo endereço de memória
	 * para apontar para o valor, sendo assim, se mudar o valor da cópia o original
	 * também muda.
	 * 
	 * O java possui o garbage colector,que exclui os objetos que não estão mais sendo
	 * referenciados, limpando a memória de forma automática
	 * */
}
	