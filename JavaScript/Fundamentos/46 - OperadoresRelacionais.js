// Exemplos
console.log('1' == 1)
// == compara o valor, nesse caso é verdadeiro
console.log('1' ===1 )
// ===(extritamente igual) compara o tipo e valor, para ser true o tipo e o valor dever sem true, nesse caso é falso
console.log('3' != 3)
// != verifica se o valor é diferente, nesse caso é falso
console.log('3' !== 3)
// !==(extritamente diferente) verifica se o tipo é diferente, nesse caso é true
console.log(null == undefined) // true
console.log(null === undefined) // false
